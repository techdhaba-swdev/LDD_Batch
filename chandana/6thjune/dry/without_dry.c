#include <stdio.h>

int main() {
    // Calculate area of two rectangles
    int length1 = 5;
    int width1 = 3;
    int area1 = length1 * width1;
    printf("Area of first rectangle: %d\n", area1);

    int length2 = 7;
    int width2 = 4;
    int area2 = length2 * width2;
    printf("Area of second rectangle: %d\n", area2);

    return 0;
}
