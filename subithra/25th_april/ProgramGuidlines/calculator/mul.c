/* mul.c */ 

/* Implementation of function related to multiplication */

int mul(int num1, int num2) //multiplication of 2 numbers
{
return num1 * num2;
}

int mul_three(int num1, int num2, int num3) //multiplication of 3 numbers
{
return num1 * num2 * num3;
}

int mul_four(int num1, int num2, int num3, int num4) ////multiplication of 4 numbers
{
return num1 * num2 * num3 * num4;
}
