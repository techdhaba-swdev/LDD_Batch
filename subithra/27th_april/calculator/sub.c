/* sub.c */ 

/* Implementation of function related to subtraction */

int sub(int num1, int num2) //subtraction of 2 numbers
{
return num1 - num2;
}

int sub_three(int num1, int num2, int num3) //subtraction of 3 numbers
{
return num1 - num2 - num3;
}

int sub_four(int num1, int num2, int num3, int num4) //subtraction of 4 numbers
{
return num1 - num2 - num3 - num4;
}
