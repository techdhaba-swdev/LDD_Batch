/* add.c */ 

/* Implementation of function related to addition */

int add(int num1, int num2) //addition of 2 numbers
{
return num1 + num2;
}

int add_three(int num1, int num2, int num3)  //addition of 3 numbers
{
return num1 + num2 + num3;
}

int add_four(int num1, int num2, int num3, int num4)  //addition of 4 numbers
{
return num1 + num2 + num3 + num4;
}
