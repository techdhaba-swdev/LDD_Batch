//function to take two numbers to do multiply
int mul(int num1, int num2){
	return num1  *  num2;//print the result of multiplication
}

//function to take three  numbers to do multiply
int mul3(int num1, int num2,int num3){
        return num1 * num2 * num3;//print the result of multiplication
}

//function to take four  numbers to do multiply
int mul4(int num1, int num2, int num3, int num4){
        return num1 * num2 * num3 * num4;//print the result of multiplication
}
