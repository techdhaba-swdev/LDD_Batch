//FUNCTION TAKING TWO NUMBERS TO ADD TWO NUMBERS 
int add(int num1, int num2){
	return num1 + num2;//return the calculated sum
}
//FUNCTION TAKING THREE NUMBERS TO ADD THREE NUMBERS
int add2(int num1, int num2, int num3){
        return num1 + num2 + num3;//return the calculated sum
}
//FUNCTION TAKING FOUR NUMBERS TO PERFORM ADD 
