// Function to add two integers
int add2(int x, int y) {
    int result = x + y; // Calculate the sum of x and y
    return result; // Return the result of the addition
}

// Function to add three integers
int add3(int x, int y, int z) {
    int result = x + y + z; // Calculate the sum of x, y, and z
    return result; // Return the result of the addition
}

// Function to add four integers
int add4(int x, int y, int z, int a) {
    int result = x + y + z + a; // Calculate the sum of x, y, z, and a
    return result; // Return the result of the addition
}
