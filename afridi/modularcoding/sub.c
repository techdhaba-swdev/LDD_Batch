// Function to subtract two integers
int sub2(int x, int y) {
    int result = x - y; // Calculate the difference of x and y
    return result; // Return the result of the subtraction
}

// Function to subtract three integers
int sub3(int x, int y, int z) {
    int result = x - y - z; // Calculate the difference of x, y, and z
    return result; // Return the result of the subtraction
}

// Function to subtract four integers
int sub4(int x, int y, int z, int a) {
    int result = x - y - z - a; // Calculate the difference of x, y, z, and a
    return result; // Return the result of the subtraction
}
